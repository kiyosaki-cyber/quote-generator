function update(){
    document.getElementById("quotes").innerHTML = bsnQuotes[index]
    btnDisableCheck()
}

function nextQuoteBtn(){
    if(index < bsnQuotes.length - 1){
        index++
        _prevQuoteBtn.disabled = false
        update()
    }
    
}
function prevQuoteBtn(){
    if(index > 0){
        index--
        _nextQuoteBtn.disabled = false
        update()
    }
}
function btnDisableCheck(){
    if(index == 0){
        _prevQuoteBtn.disabled = true
    }
    if(index == bsnQuotes.length - 1){
        _nextQuoteBtn.disabled = true
    }
    }





















